package com.careerit.jsf.day13;

public class StaticExample {
// Static method means method can be called without creating a object using class
    public static void main(String[] args) {
        // sqrt 9, m power n, ceil, floor
        System.out.println(Math.round(9.8));
        System.out.println(Math.sqrt(9));
        System.out.println(Math.pow(9,2));
        System.out.println(Math.ceil(9.8));
        System.out.println(Math.floor(9.8));
        System.out.println(Math.round(9.5));
        System.out.println(Math.round(9.449));

    }
}
